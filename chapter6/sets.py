# set and Empty Set 

# A set is a collection of unique elements. It is unordered and mutable. Sets are defined using curly braces {} or the set() constructor.
# Creating a set
my_set = {1, 2, 3, 4, 5}
print(my_set)  # Output: {1, 2, 3, 4, 5}
# Creating an empty set
empty_set = set()
print(empty_set)  # Output: set()
